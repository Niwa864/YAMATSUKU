int g_nCntGame = 1;			//ゲームカウント

void GetGameCnt(void)
{
	JudgeScore();

	g_nCntGame ++;

	//----- 画面変移 -----
	if (g_nCntGame < 4) //3回以内の時ゲームを繰り返す
	{
		CLS(WHITE, BLACK);
		Init();
		SetMode(MODE_GAME);
	}
	else
	{
		g_nCntGame = 1;
		SetMode(MODE_RESULT); //リザルトに飛ぶ
		PlayMp3(B_GAME, 1, 0);
	}
}
int SetGameCnt(void)
{
	return g_nCntGame;
}